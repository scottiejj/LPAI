#' Add Two Numbers
#'
#' This function takes two numeric inputs and returns their sum.
#'
#' @param a A numeric value.
#' @param b A numeric value.
#' @return The sum of \code{a} and \code{b}.
#' @examples
#' add_numbers(5, 3)
#' add_numbers(-2, 10)
#' @export
add_numbers <- function(a, b) {
  return(a + b)
}
